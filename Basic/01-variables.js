// Almacenar en memoria valores que necesitamos
// var - NO SE CREAN ASÍ LAS VARIABLES YA

var helloWorld = "¡hola, JavaScript!"

console.log(helloWorld)

helloWorld = "Hola de nuevo JavaScript"
console.log(helloWorld)

// let - solo son accesibles dentro de un bloque. se usa cuando varían.
let helloWorld2 = "¡hola, JavaScript 2!"
console.log(helloWorld2)


// const - constante no varia.
// Pero no son inmutables, se verá en arrays.
const helloWorld3 = "Hola JAvaScript 3. Soy una constante"
console.log(helloWorld3)